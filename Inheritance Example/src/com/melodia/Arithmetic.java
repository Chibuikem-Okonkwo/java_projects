package com.melodia;

public class Arithmetic {

    public double getSum(double a, double b){
        
        return a+b;
    }
    
    public double getDifference(double a, double b){
        
        return a-b;
    }
    
    public double getProduct(double a, double b){
        
        return a*b;
    }
    
    public double getQuotient(double a, double b){
        
        return a/b;
    }
    
    
}
